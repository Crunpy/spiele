package fachlogik;

import java.util.Date;

public class Spiel {
	
	private int id;
	private String name;
	private Date ge;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getGe() {
		return ge;
	}
	public void setGe(Date ge) {
		this.ge = ge;
	}
}
